const pick=a=>a[Math.floor(Math.random()*a.length)];
function $(id){return document.getElementById(id)}

let busy=false,jokeTimer=null,dlDir='';

// Detect context for smarter jokes
function pickDlJoke(url, pct, speed) {
  // YouTube-specific jokes sometimes
  if (url && /youtube\.com|youtu\.be/i.test(url) && Math.random() < 0.3) {
    return pick(J_YOUTUBE);
  }
  // Slow speed jokes (under 500KB/s)
  if (speed > 0 && speed < 512000 && Math.random() < 0.35) {
    return pick(J_SLOW);
  }
  // Default download jokes
  return pick(J_DL);
}

function pickSizeJoke(total) {
  // Big file (over 500MB)
  if (total > 524288000 && Math.random() < 0.4) return pick(J_BIG);
  // Tiny file (under 1MB)
  if (total > 0 && total < 1048576 && Math.random() < 0.4) return pick(J_SMALL);
  return null;
}

document.addEventListener('DOMContentLoaded', async () => {

  const splash     = $('splash');
  const spStatus   = $('splashStatus');
  const appEl      = $('app');
  const urlIn      = $('urlInput');
  const saveIn     = $('saveInput');
  const pasteBtn   = $('pasteBtn');
  const browseBtn  = $('browseBtn');
  const acceptBox  = $('acceptBox');
  const abductBtn  = $('abductBtn');
  const ejectBtn   = $('ejectBtn');
  const sizeInfo   = $('sizeInfo');
  const statusText = $('statusText');
  const bar        = $('progressBar');
  const joke       = $('joke');

  // Title bar
  if ($('tbMin'))   $('tbMin').onclick   = () => window.aliasist.winMinimize();
  if ($('tbMax'))   $('tbMax').onclick   = () => window.aliasist.winMaximize();
  if ($('tbClose')) $('tbClose').onclick = () => window.aliasist.winClose();

  // Button ripple (cache rect on enter to avoid layout work each mousemove)
  document.querySelectorAll('.btn').forEach(b => {
    let rect = null;
    b.addEventListener('mouseenter', () => {
      rect = b.getBoundingClientRect();
    });
    b.addEventListener('mousemove', e => {
      if (!rect) rect = b.getBoundingClientRect();
      b.style.setProperty('--mx', (e.clientX - rect.left) + 'px');
      b.style.setProperty('--my', (e.clientY - rect.top) + 'px');
    });
    b.addEventListener('mouseleave', () => {
      rect = null;
    });
  });

  // Splash sequence
  const MSGS = [
    "Contacting the mothership...",
    "Warming up the abduction ray...",
    "Locating nearest cow... I mean file...",
    "Plotting course to your downloads folder...",
    "All systems go!",
  ];

  for (const m of MSGS) {
    if (spStatus) spStatus.textContent = m;
    await new Promise(r => setTimeout(r, 900));
  }
    await new Promise(r => setTimeout(r, 500));
    
  if (splash) {
    splash.classList.add('exit');
    await new Promise(r => setTimeout(r, 700));
    splash.classList.add('hidden');
  }
  if (appEl) appEl.classList.remove('hidden');

  dlDir = await window.aliasist.getDlDir();
  if (saveIn) saveIn.value = dlDir;
  if (joke) joke.textContent = pick(J_IDLE);

  // Helpers
  function fmt(b) {
    if (typeof b !== 'number' || !Number.isFinite(b)) return '';
    let n = b;
    for (const u of ['B','KB','MB','GB']) {
      if (n < 1024) return n.toFixed(1) + ' ' + u;
      n /= 1024;
    }
    return n.toFixed(1) + ' TB';
  }

  function setProgress(pct) {
    const v = typeof pct === 'number' && Number.isFinite(pct) ? pct : 0;
    const clamped = Math.max(0, Math.min(100, v));
    // CSS uses `transform: scaleX(var(--sx))` for smoother updates.
    bar.style.setProperty('--sx', String(clamped / 100));
  }

  function guessName(url) {
    try {
      let n = decodeURIComponent(new URL(url).pathname.split('/').pop() || '');
      n = n.replace(/[<>:"/\\|?*]/g, '_').trim();
      if (!n || n === '/' || n === 'watch') n = 'abductee_' + Date.now().toString(36);
      if (n.length > 90) {
        const ext = n.includes('.') ? '.' + n.split('.').pop() : '';
        n = n.substring(0, 70) + '_' + Date.now().toString(36).slice(-4) + ext;
      }
      return n;
    } catch { return 'abductee_' + Date.now().toString(36); }
  }

  const STREAM_RE = /youtube\.com|youtu\.be|vimeo\.com|dailymotion\.com|twitch\.tv|soundcloud\.com|twitter\.com|x\.com|instagram\.com|tiktok\.com|facebook\.com|reddit\.com|streamable\.com|rumble\.com/i;

  function onUrl() {
    const u = urlIn.value.trim();
    // Auto-add https:// if missing
    if (u && !/^https?:\/\//i.test(u)) {
      urlIn.value = 'https://' + u;
    }
    const fullUrl = urlIn.value.trim();
    if (!fullUrl.startsWith('http://') && !fullUrl.startsWith('https://')) return;
    const name = guessName(fullUrl);
    const sep = dlDir.endsWith('/') || dlDir.endsWith('\\') ? '' : '/';
    saveIn.value = dlDir + sep + name;
    if (STREAM_RE.test(fullUrl)) {
      statusText.textContent = '🎬 Streaming target detected — yt-dlp will handle this.';
      if (/youtube\.com|youtu\.be/i.test(fullUrl)) {
        joke.textContent = pick(J_YOUTUBE);
      }
    } else {
      statusText.textContent = '🎯 Target locked. Ready to abduct.';
      joke.textContent = pick(J_IDLE);
    }
  }

  // URL events
  urlIn.addEventListener('keydown', e => { if (e.key === 'Enter') onUrl(); });
  urlIn.addEventListener('input', () => {
    const v = urlIn.value.trim();
    if (v.startsWith('http://') || v.startsWith('https://') || v.includes('.')) onUrl();
  });

  pasteBtn.onclick = async () => {
    try {
      const t = await navigator.clipboard.readText();
      if (t.trim()) {
        let url = t.trim();
        if (!/^https?:\/\//i.test(url) && url.includes('.')) {
          url = 'https://' + url;
        }
        if (url.startsWith('http://') || url.startsWith('https://')) {
          urlIn.value = url;
          onUrl();
          joke.textContent = pick(J_PASTE);
        }
      }
    } catch {}
  };

  browseBtn.onclick = async () => {
    joke.textContent = pick(J_BROWSE);
    const p = await window.aliasist.browseSave(guessName(urlIn.value || ''));
    if (p) { saveIn.value = p; statusText.textContent = '📍 Landing zone set.'; }
  };

  // Checkbox joke
  acceptBox.addEventListener('change', () => {
    if (acceptBox.checked) {
      joke.textContent = pick(J_CHECKBOX);
    }
  });

  // Track current download info for context-aware jokes
  let currentUrl = '';
  let currentSpeed = 0;
  let currentTotal = 0;

  // Progress
  window.aliasist.onProgress(d => {
    currentSpeed = d.speed || 0;
    if (d.ytInfo) {
      setProgress(d.pct);
      statusText.textContent = '🛸 Abducting... ' + d.pct.toFixed(1) + '% — ' + d.ytInfo;
      sizeInfo.textContent = d.pct.toFixed(1) + '%';
    } else if (d.pct > 0) {
      setProgress(d.pct);
      const speedTxt = fmt(d.speed);
      const pctTxt = d.pct.toFixed(1);
      statusText.textContent = speedTxt
        ? '🛸 Abducting... ' + pctTxt + '% — ' + speedTxt + '/s'
        : '🛸 Abducting... ' + pctTxt + '%';

      if (d.downloaded != null || d.total != null) {
        const dlTxt = fmt(d.downloaded);
        const totalTxt = fmt(d.total);
        sizeInfo.textContent =
          dlTxt && totalTxt ? dlTxt + ' / ' + totalTxt : (totalTxt || dlTxt);
      } else {
        sizeInfo.textContent = '';
      }
    } else {
      setProgress(0);
      statusText.textContent = '🛸 Abducting...';
      sizeInfo.textContent = '';
    }
  });

  window.aliasist.onMeta(d => {
    if (d.finalPath) saveIn.value = d.finalPath;
    if (d.total) {
      currentTotal = d.total;
      const sizeJoke = pickSizeJoke(d.total);
      if (sizeJoke) joke.textContent = sizeJoke;
    }
  });
  window.aliasist.onStatus(d => { if (d.text) statusText.textContent = d.text; });

  // Eject
  ejectBtn.onclick = () => {
    window.aliasist.abortDownload();
    statusText.textContent = '🚨 Abduction aborted!';
    joke.textContent = pick(J_ABORT);
    setProgress(0);
    sizeInfo.textContent = '';
    reset();
  };
   
  // Download
  abductBtn.onclick = async () => {
    if (busy) return;
    let url = urlIn.value.trim();

    // Auto-add protocol if missing
    if (url && !/^https?:\/\//i.test(url) && url.includes('.')) {
      url = 'https://' + url;
      urlIn.value = url;
    }

    if (!url.startsWith('http://') && !url.startsWith('https://')) {
      statusText.textContent = '⚠️ Enter a valid URL to abduct.';
      joke.textContent = "The Greys can't abduct thin air. Give them a URL.";
      return;
    }

    const save = saveIn.value.trim();
    if (!save || save === dlDir) onUrl();
    if (!acceptBox.checked) {
      statusText.textContent = '⚠️ Accept the disclaimer first.';
      joke.textContent = "Even aliens have paperwork. Check the box.";
      return;
    }

    busy = true;
    currentUrl = url;
    currentSpeed = 0;
    currentTotal = 0;
    abductBtn.disabled = true;
    abductBtn.textContent = '🛸 Abducting...';
    abductBtn.classList.add('working');
    ejectBtn.disabled = false;
    setProgress(0);
    sizeInfo.textContent = '';
    statusText.textContent = '🛸 Tractor beam engaged!';

    // First joke based on what they're downloading
    if (/youtube\.com|youtu\.be/i.test(url)) {
      joke.textContent = pick(J_YOUTUBE);
    } else {
      joke.textContent = pick(J_DL);
    }

    // Rotate context-aware jokes during download
    jokeTimer = setInterval(() => {
      joke.textContent = pickDlJoke(currentUrl, 0, currentSpeed);
    }, 3500);

    try {
      const r = await window.aliasist.downloadFile(url, saveIn.value);
      setProgress(100);
      statusText.textContent = '✅ Abduction successful! Cargo secured.';
      joke.textContent = pick(J_DONE);
      sizeInfo.textContent = '📦 Cargo: ' + fmt(r.size);
      saveIn.value = r.path;
    } catch (err) {
      if (!err.message.includes('abort') && !err.message.includes('destroy')) {
        statusText.textContent = '❌ Abduction failed!';
        joke.textContent = pick(J_ERR);
        // Show more detail so we can see the real yt-dlp error.
        sizeInfo.textContent = String(err.message).slice(0, 400);
      }
    } finally {
      clearInterval(jokeTimer);
      reset();
    }
  };

  function reset() {
    busy = false;
    currentUrl = '';
    currentSpeed = 0;
    currentTotal = 0;
    abductBtn.disabled = false;
    abductBtn.textContent = '🛸 Abduct File';
    abductBtn.classList.remove('working');
    setProgress(0);
    ejectBtn.disabled = true;
  }

});